								<div class="commonBlog">
									<h3 class="blueTitle">高尾メソッドにもとづく療養・復帰準備状況報告書の提出､<br>受領書の発行のやり取りを、SaaS化で業務効率化<span class="subGray">休職者側も人事労務担当者側も、最適化されたインターフェースでカンタン操作</span></h3>
									<div class="infoList">
										<p class="blueText">休職者管理システム（週一報告システム）とは…?</p>
										<ul class="firstList">
											<li>復職プログラム「高尾メソッド」に基づく、手書き・紙ベースでのやり取りをSaaS化。</li>
											<li>企業や自治体の休職者と上司や人事労務担当者の双方がシステム上で報告書提出・受領書発行のやり取りを行う。</li>
											<li>管理ページには､それまでの報告書と受領書の履歴が残ることで､担当者の交代の際にも引き継ぎが容易。</li>
										</ul>
										<ul class="secList">
											<li>医療的アプローチではなく､業務的アプローチを用いることで､休職者対応そのものから人事総務担当者が自信を持って行えるようにするだけではなく､復職後の予後を大幅に改善する（再療養減少など）ことのできる「高尾メソッド」｡</li>
											<li>休職者対応の中でも､従来のプログラムと対比した場合に､非常に特徴的でもある､週1回の休職者からの「療養・復帰準備状況報告」および､それに対する会社側からの受領書の発行のやりとり｡</li>
											<li>このやりとりにより､休職者は復職基準を常に意識しながら､しっかりとした復帰準備を自ら主体的に考え､取り組んでいくことで､復職後に再療養に至るなどの負の事象の発生が大幅に減少するだけでなく､なにより復職後の業務パフォーマンスが期待できる｡</li>
											<li>このキモとも言える部分が､週1回の報告とそれに対するフィードバックであることは言わずもがなではあったものの､一方で企業規模が大きくなり､同時の休職者数が数十名を超えるようになると､やるべきだとわかっていても､作業量の点から取り組むことができなかった点について､システムが支援､解決する｡</li>
										</ul>
